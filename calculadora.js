// funcion sumar
function sumar (num1, num2){
    return (num1 + num2);
}

console.log (sumar (2,3));

// funcion restar

function restar (num1, num2){
    return (num1 - num2);
}

console.log (restar (2,3));

// funcion multiplicar
function multiplicar (num1, num2){
    return (num1 * num2);
}

console.log (multiplicar (2,3));

// funcion dividir 

function dividir (num1, num2){
    return (num1 == num2);
}

console.log (dividir (0,"0"));

